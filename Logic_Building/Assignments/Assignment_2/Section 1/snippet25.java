 class Switch { 
    public static void main(String[] args) { 
        double score = 85.0; 
        if (score == 100) { 
            System.out.println("Perfect score!"); 
        } else if (score == 85) { 
            System.out.println("Great job!"); 
        } else { 
            System.out.println("Keep trying!"); 
        } 
    } 
}
