 class Confusion { 
    public static void main(String[] args) { 
        int value = 2; 
        switch(value) { 
            case 1: 
                System.out.println("Value is 1"); 
                break; // End the switch block for case 1
            case 2: 
                System.out.println("Value is 2"); 
                break; // End the switch block for case 2
            case 3: 
                System.out.println("Value is 3"); 
                break; // End the switch block for case 3
            default: 
                System.out.println("Default case"); 
        } 
    } 
}
